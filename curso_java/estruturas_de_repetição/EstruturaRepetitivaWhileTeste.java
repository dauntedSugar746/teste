package curso_java.estruturas_de_repetição;
public class EstruturaRepetitivaWhileTeste {
    public static void main(String[] args) {

        int cont = 11;
        
        while (cont != 0) {
            
            cont --;

            System.out.println(cont);


        }

        System.out.println("feliz ano novo !!!");

    }

}
