package curso_java.usos_da_memoria.classes;

public class Array2Classe {

    private String nome;
    private double preço;

    public Array2Classe(String nome, double preço){

        this.nome = nome;
        this.preço = preço;

    }

    public String getNome(){

        return nome;

    }

    public void setNome(String nome){

        this.nome = nome;

    }

    public double getPreço(){

        return preço;

    }

    public void setPreço(double preço){

        this.preço = preço;

    }

    
}
